package cn.GnaixEuy.security.config;

import cn.GnaixEuy.security.filter.TokenAuthenticationFilter;
import cn.GnaixEuy.security.filter.TokenLoginFilter;
import cn.GnaixEuy.security.security.DefaultPasswordEncoder;
import cn.GnaixEuy.security.security.TokenLogoutHandler;
import cn.GnaixEuy.security.security.TokenManager;
import cn.GnaixEuy.security.security.UnauthorizedEntryPoint;
import org.springframework.context.annotation.Configuration;
import org.springframework.data.redis.core.RedisTemplate;
import org.springframework.security.config.annotation.authentication.builders.AuthenticationManagerBuilder;
import org.springframework.security.config.annotation.method.configuration.EnableGlobalMethodSecurity;
import org.springframework.security.config.annotation.web.builders.HttpSecurity;
import org.springframework.security.config.annotation.web.builders.WebSecurity;
import org.springframework.security.config.annotation.web.configuration.EnableWebSecurity;
import org.springframework.security.config.annotation.web.configuration.WebSecurityConfigurerAdapter;
import org.springframework.security.core.userdetails.UserDetailsService;

